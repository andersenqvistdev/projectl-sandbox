def fire_pending_approvals_notification(count: int, idea_ids: list[str]) -> None:
    """Fire one notification via notifications.escalation machinery for pending ideas.

    Reads notifications.escalation from forge-config.json (same config key as
    escalation.py uses).  Both channels are off by default — this is a no-op
    unless explicitly enabled.  Any error is swallowed; it must never affect
    the queued ideas or the ideation result.
    """
    try:
        esc = _ensure_escalation()
        if esc is None:
            return

        notif_cfg = esc._get_opt_in_notifications_config()
        title = f"{count} idea(s) pending approval"

        osascript_cfg = notif_cfg.get("osascript", {})
        if osascript_cfg.get("enabled", False):
            esc._send_osascript_notification(
                task_id="ideation-batch",
                tier=1,
                reason="pending_approval",
                task_title=title,
            )

        webhook_cfg = notif_cfg.get("webhook", {})
        if webhook_cfg.get("enabled", False) and webhook_cfg.get("url"):
            payload: dict = {
                "event": "ideas_pending_approval",
                "count": count,
                "idea_ids": idea_ids[:10],
            }
            esc._send_webhook_notification(
                webhook_cfg["url"],
                payload,
                webhook_cfg.get("headers", {}),
            )
    except Exception:
        pass  # Never affect ideation handling


class IdeationType(Enum):
    """Types of creative ideas employees can generate."""

    PROCESS_IMPROVEMENT = "process_improvement"
    BOTTLENECK_OBSERVATION = "bottleneck_observation"
    CAPABILITY_EXPANSION = "capability_expansion"
    COLLABORATION_PROPOSAL = "collaboration_proposal"
    TOOL_SUGGESTION = "tool_suggestion"
    ARCHITECTURE_INSIGHT = "architecture_insight"
    STRATEGIC_OBSERVATION = "strategic_observation"


@dataclass
class Idea:
    """A creative idea generated by an employee."""

    idea_id: str
    employee_id: str
    idea_type: IdeationType
    title: str
    description: str
    rationale: str  # Why the employee thinks this is valuable
    evidence: list[dict]  # Supporting data from their experience
    estimated_value: float  # 0.0 to 1.0
    estimated_effort: str  # trivial, standard, complex
    created_at: str
    status: str = "proposed"  # proposed, approved, rejected, implemented
    related_tasks: list[str] = field(default_factory=list)
    related_employees: list[str] = field(default_factory=list)
    tags: list[str] = field(default_factory=list)

    def to_dict(self) -> dict:
        """Convert to dictionary for JSON serialization."""
        d = asdict(self)
        d["idea_type"] = self.idea_type.value
        return d

    @classmethod
    def from_dict(cls, data: dict) -> "Idea":
        """Create from dictionary."""
        data = data.copy()
        if isinstance(data.get("idea_type"), str):
            data["idea_type"] = IdeationType(data["idea_type"])
        return cls(**data)


@dataclass
class ProductContext:
    """Product context extracted from .company/vision.md goals."""

    goals: list  # list[GoalDefinition]
    active_goal_names: list[str]
    active_metrics: list[str]
    is_valid: bool  # False when vision.md is absent or has no parseable goals


def get_company_dir() -> Path:
    """Get the company directory path."""
    _ensure_imports()
    return company_resolver.get_company_dir()


def load_product_context() -> ProductContext:
    """Load product context from .company/vision.md.

    Returns ProductContext with is_valid=False if vision.md is absent or has
    no parseable goal table rows, so callers can gate idea generation on it.
    """
    _ensure_imports()
    company_dir = get_company_dir()
    vision_path = company_dir / "vision.md"

    if not vision_path.exists():
        return ProductContext(
            goals=[], active_goal_names=[], active_metrics=[], is_valid=False
        )

    try:
        goals = goal_tracker.parse_goals_from_vision(vision_path, period="active")
        # parse_goals_from_vision returns DEFAULT_GOALS (not empty) when
        # the file has no parseable table rows — detect that via identity check.
        if goals is goal_tracker.DEFAULT_GOALS:
            goals = goal_tracker.parse_goals_from_vision(vision_path, period="all")
        if not goals or goals is goal_tracker.DEFAULT_GOALS:
            print(
                f"warning: {vision_path} has no parseable goal table; ideation disabled."
                " Add goals under a period header, e.g.:"
                " ### Period: Q1 2026 [status: active]"
                " | G1: Goal Name | Description | Success metric | owner |",
                file=sys.stderr,
            )
            return ProductContext(
                goals=[], active_goal_names=[], active_metrics=[], is_valid=False
            )
        return ProductContext(
            goals=goals,
            active_goal_names=[g.name for g in goals],
            active_metrics=[g.success_metric for g in goals if g.success_metric],
            is_valid=True,
        )
    except Exception:
        return ProductContext(
            goals=[], active_goal_names=[], active_metrics=[], is_valid=False
        )


def get_ideas_path() -> Path:
    """Get path to ideas storage file."""
    return get_company_dir() / "employee_ideas.json"


def load_ideas() -> dict:
    """Load ideas from storage."""
    path = get_ideas_path()
    if not path.exists():
        return {
            "ideas": [],
            "metadata": {"created_at": datetime.now(timezone.utc).isoformat()},
        }
    try:
        with open(path) as f:
            return json.load(f)
    except (json.JSONDecodeError, OSError):
        return {
            "ideas": [],
            "metadata": {"created_at": datetime.now(timezone.utc).isoformat()},
        }


def save_ideas(data: dict) -> None:
    """Save ideas to storage with atomic write."""
    import os
    import tempfile

    path = get_ideas_path()
    data["metadata"]["last_modified"] = datetime.now(timezone.utc).isoformat()

    # Atomic write
    dir_path = path.parent
    dir_path.mkdir(parents=True, exist_ok=True)
    fd, tmp_path = tempfile.mkstemp(dir=dir_path, suffix=".json")
    try:
        with os.fdopen(fd, "w") as f:
            json.dump(data, f, indent=2)
        os.replace(tmp_path, path)
    except Exception:
        if os.path.exists(tmp_path):
            os.unlink(tmp_path)
        raise


def is_duplicate_idea(title: str, idea_type: str) -> tuple[bool, str | None]:
    """
    Check if an idea is a duplicate of an existing pending proposal or queued task.

    Returns (is_duplicate, reason) tuple.
    """
    # Normalize title for comparison
    title_lower = title.lower()

    # Extract the key pattern (e.g., "documentation" from "Automate repeated 'documentation' tasks")
    pattern_key = None
    if "repeated" in title_lower and "'" in title:
        # Extract quoted pattern
        start = title.find("'")
        end = title.find("'", start + 1)
        if start != -1 and end != -1:
            pattern_key = title[start + 1 : end].lower()

    # Check pending proposals in initiative engine
    try:
        pending_path = get_company_dir() / "state/pending_approvals.json"
        if pending_path.exists():
            with open(pending_path) as f:
                pending_data = json.load(f)

            for prop in pending_data.get("pending", []):
                prop_title = prop.get("title", "").lower()
                # Check for similar titles
                if pattern_key and pattern_key in prop_title:
                    return True, f"Similar pending proposal: {prop.get('proposal_id')}"
                if title_lower == prop_title:
                    return True, f"Exact match pending: {prop.get('proposal_id')}"
    except (json.JSONDecodeError, OSError):
        pass

    # Check pending tasks in work queue
    try:
        queue_path = get_company_dir() / "state/work_queue.json"
        if queue_path.exists():
            with open(queue_path) as f:
                queue_data = json.load(f)

            for task in queue_data.get("tasks", []):
                if task.get("status") not in ("pending", "active"):
                    continue
                task_title = task.get("title", "").lower()
                # Check for similar titles
                if pattern_key and pattern_key in task_title:
                    return True, f"Similar pending task: {task.get('task_id')}"
    except (json.JSONDecodeError, OSError):
        pass

    # Check existing ideas in employee_ideas.json
    try:
        ideas_path = get_company_dir() / "employee_ideas.json"
        if ideas_path.exists():
            with open(ideas_path) as f:
                ideas_data = json.load(f)

            for idea in ideas_data.get("ideas", []):
                idea_title = idea.get("title", "").lower()
                if pattern_key and pattern_key in idea_title:
                    return True, f"Similar existing idea: {idea.get('idea_id')}"
                if title_lower == idea_title:
                    return True, f"Exact match idea: {idea.get('idea_id')}"
    except (json.JSONDecodeError, OSError):
        pass

    return False, None


def generate_idea_id(employee_id: str, idea_type: str) -> str:
    """Generate unique idea ID."""
    timestamp = datetime.now(timezone.utc).strftime("%Y%m%d%H%M%S")
    hash_input = f"{employee_id}-{idea_type}-{timestamp}"
    short_hash = hashlib.sha256(hash_input.encode()).hexdigest()[:6]
    return f"idea-{timestamp}-{short_hash}"


