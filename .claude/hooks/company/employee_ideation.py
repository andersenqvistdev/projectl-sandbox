def generate_strategic_observation_idea(
    employee_id: str,
    task_history: list[dict],
    patterns: dict,
) -> Idea | None:
    """Generate strategic observation from work patterns."""
    if patterns["total"] < 10:
        return None

    employee = get_employee(employee_id)
    employee_name = employee.get("name", employee_id) if employee else employee_id

    # Look at complexity distribution
    complexity_dist = patterns.get("complexity_distribution", {})
    total = sum(complexity_dist.values())

    if total < 10:
        return None

    # Check for complexity imbalance
    complex_ratio = (
        complexity_dist.get("complex", 0) + complexity_dist.get("epic", 0)
    ) / total
    trivial_ratio = complexity_dist.get("trivial", 0) / total

    if complex_ratio > 0.3:
        return Idea(
            idea_id=generate_idea_id(employee_id, "strategic"),
            employee_id=employee_id,
            idea_type=IdeationType.STRATEGIC_OBSERVATION,
            title="High complexity workload may indicate need for decomposition",
            description=(
                f"{int(complex_ratio * 100)}% of my recent tasks were complex or epic. "
                f"This high ratio suggests we may benefit from better task decomposition "
                f"at the planning stage to enable more parallel execution."
            ),
            rationale=(
                f"As {employee_name}, I'm observing that many tasks arrive already complex. "
                f"Breaking them down earlier would improve throughput and reduce risk."
            ),
            evidence=[
                {"complexity_distribution": complexity_dist, "total_tasks": total}
            ],
            estimated_value=0.6,
            estimated_effort="standard",
            created_at=datetime.now(timezone.utc).isoformat(),
            tags=["strategic", "planning", "decomposition"],
        )

    if trivial_ratio > 0.5:
        return Idea(
            idea_id=generate_idea_id(employee_id, "strategic"),
            employee_id=employee_id,
            idea_type=IdeationType.STRATEGIC_OBSERVATION,
            title="High trivial task ratio may indicate automation opportunity",
            description=(
                f"{int(trivial_ratio * 100)}% of my recent tasks were trivial. "
                f"This suggests many tasks could potentially be automated or batched."
            ),
            rationale=(
                f"As {employee_name}, spending time on many trivial tasks suggests "
                f"we could free up capacity by automating routine work."
            ),
            evidence=[
                {"complexity_distribution": complexity_dist, "total_tasks": total}
            ],
            estimated_value=0.5,
            estimated_effort="standard",
            created_at=datetime.now(timezone.utc).isoformat(),
            tags=["strategic", "automation", "efficiency"],
        )

    return None


def run_ideation_for_employee(
    employee_id: str,
    max_ideas: int = 3,
    dry_run: bool = False,
) -> list[Idea]:
    """Run creative ideation for a single employee.

    Primary path: goal decomposition from active vision.md goals — each idea
    advances a specific goal referenced by ID and target metric, so the
    company ideates on its product, not on itself.

    Fallback: task-history template analysis when no valid product context
    exists (vision.md absent or has no parseable goal table).

    Returns list of ideas generated (not necessarily saved).
    """
    employee = get_employee(employee_id)
    if not employee:
        return []

    product_ctx = load_product_context()

    if product_ctx.is_valid:
        # Primary: goal-decomposition — all employees contribute to shared product goals
        ideas = generate_goal_decomposition_ideas(
            employee_id, employee, product_ctx, max_ideas
        )
        ideas = [
            idea
            for idea in ideas
            if not is_duplicate_idea(idea.title, idea.idea_type.value)[0]
        ]
    else:
        # Fallback: task-history template analysis (no valid vision.md)
        efficiency = employee.get("efficiency", {})
        if efficiency.get("tasks_completed", 0) < 3:
            # Safe default: no product context + no task history → no ideas
            ideas = []
        else:
            task_history = get_employee_task_history(employee_id, days=30)
            patterns = analyze_task_patterns(task_history)
            bottlenecks = analyze_bottlenecks(employee_id)
            capability_gaps = analyze_capability_gaps(employee_id)
            collaboration_opps = find_collaboration_opportunities(employee_id)

            ideas = []

            process_idea = generate_process_improvement_idea(
                employee_id, patterns, task_history
            )
            if process_idea:
                ideas.append(process_idea)

            bottleneck_idea = generate_bottleneck_idea(employee_id, bottlenecks)
            if bottleneck_idea:
                ideas.append(bottleneck_idea)

            capability_idea = generate_capability_expansion_idea(
                employee_id, capability_gaps
            )
            if capability_idea:
                ideas.append(capability_idea)

            collab_idea = generate_collaboration_idea(employee_id, collaboration_opps)
            if collab_idea:
                ideas.append(collab_idea)

            strategic_idea = generate_strategic_observation_idea(
                employee_id, task_history, patterns
            )
            if strategic_idea:
                ideas.append(strategic_idea)

            ideas.sort(key=lambda x: -x.estimated_value)
            ideas = ideas[:max_ideas]

            ideas = [
                idea
                for idea in ideas
                if not is_duplicate_idea(idea.title, idea.idea_type.value)[0]
            ]

    # Save and route for approval if not dry run
    if not dry_run and ideas:
        data = load_ideas()
        existing_ids = {i.get("idea_id") for i in data.get("ideas", [])}
        config = load_ideation_config()

        for idea in ideas:
            if idea.idea_id not in existing_ids:
                idea_dict = idea.to_dict()
                data["ideas"].append(idea_dict)

        save_ideas(data)

        for idea in ideas:
            route_idea_for_approval(idea.to_dict(), config)

    return ideas


