def get_employee_task_history(employee_id: str, days: int = 30) -> list[dict]:
    """Get recent tasks completed by an employee."""
    queue = load_work_queue()
    cutoff = datetime.now(timezone.utc) - timedelta(days=days)

    tasks = []
    for task in queue.get("completed", []):
        if task.get("executed_by") != employee_id:
            continue

        completed_at = task.get("completed_at")
        if completed_at:
            try:
                dt = datetime.fromisoformat(completed_at.replace("Z", "+00:00"))
                if dt >= cutoff:
                    tasks.append(task)
            except (ValueError, TypeError):
                pass

    return tasks


