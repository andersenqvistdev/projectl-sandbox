"""
Employee Creative Ideation Module — P34

Enables employees to generate creative ideas and suggestions like in a real company.
Unlike P26 (reactive TODO scanning), P34 is proactive ideation based on experience.

Ideation Types:
- process_improvement: "I've done X task 5 times, we should automate it"
- bottleneck_observation: "Task type Y keeps failing, root cause is Z"
- capability_expansion: "I keep getting tasks needing Z, I should learn it"
- collaboration_proposal: "My work overlaps with [employee], we should sync"
- tool_suggestion: "A tool for X would save time"
- architecture_insight: "These modules are coupled, should refactor"
- strategic_observation: "Pattern in backlog suggests we need X"

Usage:
    # Run ideation for a specific employee
    python employee_ideation.py ideate --employee-id "senior-python-developer"

    # Run ideation cycle for all employees
    python employee_ideation.py cycle

    # Get ideation stats
    python employee_ideation.py stats

    # List recent ideas
    python employee_ideation.py list --days 7
"""

