def _get_domain_themes_for_employee(employee: dict) -> list[dict[str, str]]:
    """Get domain-specific idea themes matching employee capabilities."""
    capabilities = employee.get("capabilities", [])
    themes: list[dict[str, str]] = []
    seen_themes: set[str] = set()
    for cap in capabilities:
        for domain_themes in DOMAIN_IDEA_THEMES.get(cap, []):
            theme_key = domain_themes["theme"]
            if theme_key not in seen_themes:
                themes.append(domain_themes)
                seen_themes.add(theme_key)
    return themes


def generate_zero_activity_ideas(
    employee_id: str, employee: dict, product_ctx: ProductContext
) -> list[Idea]:
    """
    Generate product-grounded ideas for employees with low/zero task activity.

    Requires valid product context (vision.md with parsed goals). Returns an
    empty list when product_ctx.is_valid is False so that idle employees don't
    invent generic self-improvement work unrelated to the actual product.
    """
    if not product_ctx.is_valid:
        return []

    emp_name = employee.get("name", employee_id)
    department = employee.get("department", "unknown")
    capabilities = employee.get("capabilities", [])
    cap_str = ", ".join(capabilities[:3]) if capabilities else "general skills"

    ideas: list[Idea] = []
    for goal in product_ctx.goals[:3]:  # Cap at 3 ideas
        ideas.append(
            Idea(
                idea_id=generate_idea_id(employee_id, f"vision-{goal.id}"),
                employee_id=employee_id,
                idea_type=IdeationType.STRATEGIC_OBSERVATION,
                title=f"[{department}] Advance {goal.name}: {goal.description}",
                description=(
                    f"As {emp_name} in {department}, I can contribute to the "
                    f"'{goal.name}' goal. Target: {goal.success_metric}. "
                    f"I will apply {cap_str} to help achieve this."
                ),
                rationale=(
                    f"Product goal {goal.id} ({goal.name}) is active. "
                    f"My capabilities can directly support: {goal.description}."
                ),
                evidence=[
                    {
                        "goal_id": goal.id,
                        "goal_name": goal.name,
                        "success_metric": goal.success_metric,
                    }
                ],
                estimated_value=0.6,
                estimated_effort="standard",
                created_at=datetime.now(timezone.utc).isoformat(),
                tags=[department, goal.id.lower(), "product-vision"],
            )
        )

    return ideas


def _get_covered_goal_ids(goal_ids: set[str]) -> set[str]:
    """Return lowercase goal IDs that already have open ideas or active tasks.

    A goal is "covered" when further ideation for it would produce a duplicate:
    - An idea with status "proposed" or "pending_approval" referencing that goal, OR
    - An active/pending task in the work queue tagged with that goal ID.
    """
    goal_ids_lower = {g.lower() for g in goal_ids}
    covered: set[str] = set()

    try:
        ideas_path = get_company_dir() / "employee_ideas.json"
        if ideas_path.exists():
            with open(ideas_path) as f:
                ideas_data = json.load(f)
            open_statuses = {"proposed", "pending_approval"}
            for idea in ideas_data.get("ideas", []):
                if idea.get("status") not in open_statuses:
                    continue
                if "goal-decomposition" not in idea.get("tags", []):
                    continue
                for ev in idea.get("evidence", []):
                    gid = ev.get("goal_id", "").lower()
                    if gid in goal_ids_lower:
                        covered.add(gid)
    except (json.JSONDecodeError, OSError):
        pass

    try:
        queue_path = get_company_dir() / "state/work_queue.json"
        if queue_path.exists():
            with open(queue_path) as f:
                queue_data = json.load(f)
            active_statuses = {"pending", "active"}
            for task in queue_data.get("tasks", []):
                if task.get("status") not in active_statuses:
                    continue
                for tag in task.get("tags", []):
                    if tag.lower() in goal_ids_lower:
                        covered.add(tag.lower())
    except (json.JSONDecodeError, OSError):
        pass

    return covered


def generate_goal_decomposition_ideas(
    employee_id: str,
    employee: dict,
    product_ctx: ProductContext,
    max_ideas: int = 3,
) -> list[Idea]:
    """Generate concrete next-step task ideas for each active product goal.

    Rotates across ALL goals to ensure full coverage across ideation cycles.
    Goals already covered by an open idea or active task are skipped (one open
    idea per goal cap). Uncovered goals are proposed first; goals whose owner
    matches this employee are preferred within each coverage band.

    Returns empty list when product_ctx.is_valid is False (safe default:
    no parseable goals → no ideas).
    """
    if not product_ctx.is_valid:
        return []

    emp_name = employee.get("name", employee_id)
    department = employee.get("department", "unknown")
    capabilities = employee.get("capabilities", [])
    cap_str = ", ".join(capabilities[:3]) if capabilities else "general skills"

    # Exclude goals already shipped/completed.
    active_goals = [g for g in product_ctx.goals if g.period_status != "complete"]

    # Exclude goals already verified post-merge via metric check.
    try:
        _ensure_imports()
        _ver_state = goal_tracker.load_goal_verification_state(get_company_dir())
        _verified_ids = {
            gid for gid, rec in _ver_state.items() if rec.get("status") == "verified"
        }
        active_goals = [g for g in active_goals if g.id not in _verified_ids]
    except Exception:
        pass  # Verification state unavailable — proceed without filtering

    all_goal_ids = {g.id for g in active_goals}
    covered_ids = _get_covered_goal_ids(all_goal_ids)

    def _owner_score(goal) -> int:
        if goal.owner == employee_id:
            return 2
        if goal.owner in capabilities:
            return 1
        return 0

    # Sort: uncovered first (0 < 1), then owner affinity descending, then stable index.
    sorted_goals = [
        goal
        for _, goal in sorted(
            enumerate(active_goals),
            key=lambda item: (
                int(item[1].id.lower() in covered_ids),
                -_owner_score(item[1]),
                item[0],
            ),
        )
    ]

    ideas: list[Idea] = []
    for goal in sorted_goals:
        if len(ideas) >= max_ideas:
            break
        # One open idea per goal: uncovered goals are sorted first, so the first
        # covered goal encountered means all remaining are also covered.
        if goal.id.lower() in covered_ids:
            break
        ideas.append(
            Idea(
                idea_id=generate_idea_id(employee_id, f"goal-{goal.id}"),
                employee_id=employee_id,
                idea_type=IdeationType.STRATEGIC_OBSERVATION,
                title=f"[{goal.id}] {goal.name}: advance toward {goal.success_metric}",
                description=(
                    f"Goal {goal.id} ({goal.name}) is active. "
                    f"Description: {goal.description}. "
                    f"Target: {goal.success_metric}. "
                    f"As {emp_name} ({department}, {cap_str}), "
                    f"the next concrete task is to contribute directly to this goal."
                ),
                rationale=(
                    f"Active goal {goal.id} ({goal.name}) requires: {goal.description}. "
                    f"Capabilities ({cap_str}) can directly advance this goal."
                ),
                evidence=[
                    {
                        "goal_id": goal.id,
                        "goal_name": goal.name,
                        "success_metric": goal.success_metric,
                        "description": goal.description,
                    }
                ],
                estimated_value=0.7,
                estimated_effort="standard",
                created_at=datetime.now(timezone.utc).isoformat(),
                tags=[department, goal.id.lower(), "goal-decomposition"],
            )
        )

    return ideas


