def _recover_stranded_proposed_ideas(cycle_interval_minutes: int = 30) -> int:
    """Route ideas stuck in 'proposed' from a prior crashed cycle.

    An idea stays 'proposed' when a crash occurred after save_ideas() but before
    route_idea_for_approval() completed.  Cross-run dedup blocks regenerating them,
    so they must be re-routed here.  Returns the number of ideas recovered.
    """
    data = load_ideas()
    cutoff = datetime.now(timezone.utc) - timedelta(minutes=cycle_interval_minutes)
    config = load_ideation_config()
    recovered = 0
    for idea in data.get("ideas", []):
        if idea.get("status") != "proposed":
            continue
        try:
            created_at = datetime.fromisoformat(
                idea["created_at"].replace("Z", "+00:00")
            )
        except (KeyError, ValueError):
            continue
        if created_at < cutoff:
            route_idea_for_approval(idea, config)
            recovered += 1
    return recovered


def run_ideation_cycle(
    max_employees: int = 3,
    max_ideas_per_employee: int = 2,
    dry_run: bool = False,
    target_zero_activity: bool = False,
) -> dict:
    """
    Run ideation cycle for multiple employees.

    When target_zero_activity is True, targets under-represented employees
    (tasks_completed < 3) with domain-specific prompts instead of the
    usual high-activity employees.

    Returns summary of ideas generated.
    """
    if not dry_run:
        _recover_stranded_proposed_ideas()

    org = load_org()
    employees = org.get("employees", [])

    if not employees:
        print(
            "warning: no employees configured in org.json; ideation cycle skipped."
            " Run /company-hire to add employees first.",
            file=sys.stderr,
        )
        return {
            "employees_processed": [],
            "ideas_generated": [],
            "total_ideas": 0,
            "auto_approved": 0,
            "pending_manager": 0,
            "pending_executive": 0,
            "dry_run": dry_run,
            "timestamp": datetime.now(timezone.utc).isoformat(),
        }

    if target_zero_activity:
        # Target under-represented employees with < 3 completed tasks
        zero_activity = []
        for emp in employees:
            efficiency = emp.get("efficiency", {})
            tasks_completed = efficiency.get("tasks_completed", 0)
            if tasks_completed < 3:
                zero_activity.append((emp, tasks_completed))
        # Sort: partial activity first, then alphabetical for determinism
        zero_activity.sort(key=lambda x: (-x[1], x[0].get("id", "")))

        # Apply rotation: filter out recently targeted employees
        rotation = _load_ideation_rotation()
        recently_targeted = set(rotation.get("recently_targeted", []))
        all_zero_ids = {e[0].get("id") for e in zero_activity}

        # If all zero-activity employees have been recently targeted, reset
        if all_zero_ids and all_zero_ids.issubset(recently_targeted):
            recently_targeted = set()
            rotation["recently_targeted"] = []

        # Filter out recently targeted
        filtered = [
            (emp, tc)
            for emp, tc in zero_activity
            if emp.get("id") not in recently_targeted
        ]
        selected = [e[0] for e in filtered[:max_employees]]

        # Update rotation state with newly targeted employee IDs
        newly_targeted = [emp.get("id") for emp in selected if emp.get("id")]
        rotation["recently_targeted"] = list(recently_targeted) + newly_targeted
        _save_ideation_rotation(rotation)
    else:
        # Process ALL employees: high-activity ones use task-history ideation,
        # low-activity ones get product-grounded ideation from the shared goal set.
        employees_with_count = []
        for emp in employees:
            efficiency = emp.get("efficiency", {})
            tasks_completed = efficiency.get("tasks_completed", 0)
            employees_with_count.append((emp, tasks_completed))
        # Sort: high-activity first, then alphabetical for determinism
        employees_with_count.sort(key=lambda x: (-x[1], x[0].get("id", "")))
        selected = [e[0] for e in employees_with_count[:max_employees]]

    results = {
        "employees_processed": [],
        "ideas_generated": [],
        "total_ideas": 0,
        "auto_approved": 0,
        "pending_manager": 0,
        "pending_executive": 0,
        "dry_run": dry_run,
        "timestamp": datetime.now(timezone.utc).isoformat(),
    }

    for emp in selected:
        emp_id = emp.get("id")
        ideas = run_ideation_for_employee(
            emp_id,
            max_ideas=max_ideas_per_employee,
            dry_run=dry_run,
        )

        results["employees_processed"].append(emp_id)
        for idea in ideas:
            # Get approval level for tracking
            level = get_approval_level(idea)

            results["ideas_generated"].append(
                {
                    "idea_id": idea.idea_id,
                    "employee_id": idea.employee_id,
                    "type": idea.idea_type.value,
                    "title": idea.title,
                    "value": idea.estimated_value,
                    "approval_level": level.value,
                }
            )

            # Track by approval level
            if level == ApprovalLevel.AUTO:
                results["auto_approved"] += 1
            elif level == ApprovalLevel.MANAGER:
                results["pending_manager"] += 1
            else:
                results["pending_executive"] += 1

        results["total_ideas"] += len(ideas)

    # Fire one notification if any ideas entered pending_approval this batch
    pending_count = results["pending_manager"] + results["pending_executive"]
    if pending_count > 0 and not dry_run:
        queued_ids = [
            i["idea_id"]
            for i in results["ideas_generated"]
            if i.get("approval_level") in ("manager", "executive")
        ]
        fire_pending_approvals_notification(pending_count, queued_ids)

    return results


def get_ideation_stats() -> dict:
    """Get statistics about employee ideation."""
    data = load_ideas()
    ideas = data.get("ideas", [])

    now = datetime.now(timezone.utc)
    day_ago = now - timedelta(days=1)
    week_ago = now - timedelta(days=7)

    stats = {
        "total_ideas": len(ideas),
        "ideas_last_24h": 0,
        "ideas_last_week": 0,
        "by_type": {},
        "by_employee": {},
        "by_status": {},
        "avg_value": 0.0,
    }

    total_value = 0.0

    for idea in ideas:
        # Count by time
        created_at = idea.get("created_at", "")
        try:
            dt = datetime.fromisoformat(created_at.replace("Z", "+00:00"))
            if dt >= day_ago:
                stats["ideas_last_24h"] += 1
            if dt >= week_ago:
                stats["ideas_last_week"] += 1
        except (ValueError, TypeError):
            pass

        # Count by type
        idea_type = idea.get("idea_type", "unknown")
        stats["by_type"][idea_type] = stats["by_type"].get(idea_type, 0) + 1

        # Count by employee
        emp_id = idea.get("employee_id", "unknown")
        stats["by_employee"][emp_id] = stats["by_employee"].get(emp_id, 0) + 1

        # Count by status
        status = idea.get("status", "proposed")
        stats["by_status"][status] = stats["by_status"].get(status, 0) + 1

        # Sum value
        total_value += idea.get("estimated_value", 0.0)

    if ideas:
        stats["avg_value"] = round(total_value / len(ideas), 2)

    return stats


def list_recent_ideas(days: int = 7, status: str | None = None) -> list[dict]:
    """List recent ideas."""
    data = load_ideas()
    ideas = data.get("ideas", [])

    cutoff = datetime.now(timezone.utc) - timedelta(days=days)

    results = []
    for idea in ideas:
        # Filter by time
        created_at = idea.get("created_at", "")
        try:
            dt = datetime.fromisoformat(created_at.replace("Z", "+00:00"))
            if dt < cutoff:
                continue
        except (ValueError, TypeError):
            continue

