    if evidence:
        description_parts.append("\n\n**Evidence:**")
        for ev in evidence[:5]:  # Include up to 5 evidence items inline
            if isinstance(ev, dict):
                ev_type = ev.get("type", "observation")
                ev_data = ev.get("data", ev.get("description", str(ev)))
                description_parts.append(f"\n- {ev_type}: {ev_data}")
            else:
                description_parts.append(f"\n- {ev}")
        if len(evidence) > 5:
            description_parts.append(
                f"\n- ... and {len(evidence) - 5} more (see reference doc)"
            )

    # Add related tasks (so implementer knows what to reference)
    related_tasks = idea.get("related_tasks", [])
    if related_tasks:
        description_parts.append(
            f"\n\n**Related Tasks:** {', '.join(related_tasks[:5])}"
        )
        if len(related_tasks) > 5:
            description_parts.append(f" (and {len(related_tasks) - 5} more)")

    # Add related employees (for coordination)
    related_employees = idea.get("related_employees", [])
    if related_employees:
        description_parts.append(
            f"\n\n**Coordinate With:** {', '.join(related_employees)}"
        )

    # Reference the full idea document
    if ref_doc_path:
        description_parts.append(
            f"\n\n**Full Context:** See `.company/ideas/{idea_id}.md`"
        )

    task = {
        "task_id": f"task-{idea_id.replace('idea-', '')}",
        "title": idea.get("title", "Implement employee idea"),
        "description": "".join(description_parts),
        "priority": priority_map.get(idea_type, 3),
        "required_capabilities": idea.get("tags", []),  # Full tags, not truncated
        "estimated_complexity": effort_map.get(
            idea.get("estimated_effort", "standard"), "standard"
        ),
        "source": "employee_ideation",
        "source_idea_id": idea_id,
        "proposed_by": idea.get("employee_id"),
        # NEW: Preserve full context metadata
        "idea_reference_doc": ref_doc_path,
        "related_tasks": related_tasks,
        "related_employees": related_employees,
        "estimated_value": idea.get("estimated_value"),
        "idea_type": idea_type,
    }

    return task


