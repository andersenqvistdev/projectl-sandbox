def _title_pattern_words(title: str) -> list[str]:
    """Discriminating tokens for pattern-frequency analysis.

    Delegates to learned_antipatterns.signature(), which already strips
    stopwords (e.g. "for", "test", "add") that caused analyze_task_patterns
    to mistake shared title phrasing (e.g. "... for: X") for a repeated,
    automatable task class. Falls back to naive splitting if the import
    fails, so this never hard-fails the caller.
    """
    try:
        try:
            from . import learned_antipatterns as la  # type: ignore[attr-defined]
        except ImportError:
            import learned_antipatterns as la  # type: ignore[no-redef]
        return la.signature(title)
    except Exception:
        return [w for w in title.lower().split() if len(w) > 3]


def analyze_task_patterns(tasks: list[dict]) -> dict:
    """Analyze patterns in employee's task history."""
    if not tasks:
        return {"total": 0, "patterns": []}

    # Count task types and titles
    title_words = {}
    complexity_dist = {}
    capabilities_used = {}

    for task in tasks:
        # Extract title patterns
        title = task.get("title", "")
        for word in _title_pattern_words(title):
            if len(word) > 3:  # Skip short words
                title_words[word] = title_words.get(word, 0) + 1

        # Complexity distribution
        complexity = task.get("estimated_complexity", "standard")
        complexity_dist[complexity] = complexity_dist.get(complexity, 0) + 1

        # Capabilities
        for cap in task.get("required_capabilities", []):
            capabilities_used[cap] = capabilities_used.get(cap, 0) + 1

    # Find repeated patterns (words appearing 3+ times)
    patterns = [
        {"word": word, "count": count}
        for word, count in sorted(title_words.items(), key=lambda x: -x[1])
        if count >= 3
    ][:5]

    return {
        "total": len(tasks),
        "patterns": patterns,
        "complexity_distribution": complexity_dist,
        "capabilities_used": capabilities_used,
    }


def analyze_bottlenecks(employee_id: str) -> list[dict]:
    """Analyze bottlenecks from retry history and failures."""
    queue = load_work_queue()
    outcomes = load_learning_outcomes()

    bottlenecks = []

    # Check for tasks with retries
    for task in queue.get("completed", []):
        if task.get("executed_by") != employee_id:
            continue

        retry_count = task.get("retry_count", 0)
        if retry_count >= 2:
            bottlenecks.append(
                {
                    "type": "high_retry",
                    "task_id": task.get("task_id"),
                    "title": task.get("title"),
                    "retry_count": retry_count,
                    "severity": "medium" if retry_count < 3 else "high",
                }
            )

    # Check learning outcomes for failures
    for outcome in outcomes:
        if outcome.get("employee_id") != employee_id:
            continue
        if not outcome.get("success", True):
            bottlenecks.append(
                {
                    "type": "task_failure",
                    "task_id": outcome.get("task_id"),
                    "complexity": outcome.get("complexity"),
                    "severity": "high",
                }
            )

    return bottlenecks


def analyze_capability_gaps(employee_id: str) -> list[dict]:
    """Find capabilities the employee is often asked for but doesn't have."""
    efficiency_data = load_efficiency_data()
    employee = get_employee(employee_id)

    if not employee:
        return []

    current_caps = set(employee.get("capabilities", []))
    gaps = []

    # Check capability gap history
    gap_history = efficiency_data.get("capability_gaps", {}).get("gap_history", [])

    # Count gaps where this employee was the fallback
    gap_counts = {}
    for gap in gap_history:
        if gap.get("fallback_employee") == employee_id:
            for missing in gap.get("missing", []):
                if missing not in current_caps:
                    gap_counts[missing] = gap_counts.get(missing, 0) + 1

    # Capabilities requested 2+ times
    for cap, count in sorted(gap_counts.items(), key=lambda x: -x[1]):
        if count >= 2:
            gaps.append(
                {
                    "capability": cap,
                    "request_count": count,
                    "currently_has": False,
                }
            )

    return gaps[:5]  # Top 5 gaps


def find_collaboration_opportunities(employee_id: str) -> list[dict]:
    """Find employees with overlapping work for collaboration."""
    load_work_queue()
    org = load_org()

    employee = get_employee(employee_id)
    if not employee:
        return []

    my_caps = set(employee.get("capabilities", []))
    my_tasks = get_employee_task_history(employee_id, days=14)
    my_task_caps = set()

    for task in my_tasks:
        my_task_caps.update(task.get("required_capabilities", []))

    opportunities = []

    for emp in org.get("employees", []):
        if emp.get("id") == employee_id:
            continue

        their_caps = set(emp.get("capabilities", []))
        overlap = my_caps & their_caps

        if len(overlap) >= 2:
            # Get their recent tasks
            their_tasks = get_employee_task_history(emp.get("id"), days=14)

            if len(my_tasks) > 0 and len(their_tasks) > 0:
                opportunities.append(
                    {
                        "employee_id": emp.get("id"),
                        "employee_name": emp.get("name"),
                        "shared_capabilities": list(overlap),
                        "overlap_count": len(overlap),
                        "their_recent_task_count": len(their_tasks),
                    }
                )

    # Sort by overlap
    return sorted(opportunities, key=lambda x: -x["overlap_count"])[:3]


